package Oops;

public class Employee {

    private int id;
    private String name;

    //setter method
    public void setId(int id){
        this.id=id;

    }
    public void setName(String name){
        this.name=name;
    }

    //getter method
    public int getId(){
        return id;
    }
    public String getName(){
        return name;
    }

}
